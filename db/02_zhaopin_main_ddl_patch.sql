-- USE zhaopin_db;   -- ← 按实际主库名取消注释

-- ------------------------------------------------------------
-- 改动一：招聘表增加去重列（用于三级去重比对，见文档 §3、§4.4）
-- ------------------------------------------------------------
ALTER TABLE `jobs`
    ADD COLUMN `contact_key` VARCHAR(70)     NULL COMMENT '电话/微信归一化去重键 phone_norm|wechat_norm',
    ADD COLUMN `simhash`     BIGINT UNSIGNED NULL COMMENT '内容指纹 SimHash(64bit)',
    ADD KEY `idx_contact_key` (`contact_key`),
    ADD KEY `idx_simhash` (`simhash`);

-- 注意：存量数据需回填一次 contact_key / simhash
-- （运行采集器提供的一次性回填脚本：php app/bin/backfill_main.php --dry-run）
-- 否则采集器只能与“改动后新增”的主库记录去重。
-- 主站新发布招聘应在写入时同步生成这两列。

-- ------------------------------------------------------------
-- 改动二：招聘表增加来源标记（用于清理时双保险校验，见文档 §5.6、§8）
-- ------------------------------------------------------------
ALTER TABLE `jobs`
    ADD COLUMN `origin` VARCHAR(20) NOT NULL DEFAULT 'user'
        COMMENT '数据来源：user=自有UGC / crawler=冷启动采集导入',
    ADD KEY `idx_origin` (`origin`);

-- 采集数据导入主库时置 origin='crawler'；自有用户发布保持 origin='user'。
-- 清理时以采集库 cj_import_map 为账本精准定位，origin='crawler' 作双保险，
-- 防止误删自有 UGC。

